package com.CABA.CabaPro.model;

/**
 * Enumeración que representa los posibles estados de una asignación.
 */
public enum EstadoAsignacionEnum {

    /**
     * Estado que indica que la asignación ha sido aceptada.
     */
    ACEPTADA,

    /**
     * Estado que indica que la asignación está pendiente de respuesta.
     */
    PENDIENTE,

    /**
     * Estado que indica que la asignación ha sido rechazada.
     */
    RECHAZADA
}
